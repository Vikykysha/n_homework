
CREATE TABLE filmes (
    TITLE VARCHAR(512),
    ID INTEGER PRIMARY KEY,
    COUNTRY VARCHAR(512),
    BOX_OFFICE NUMERIC,
    RELEASE_YEAR date --!!
);

CREATE TABLE persons (
    ID INTEGER PRIMARY KEY,
    FIO VARCHAR(512)
);

CREATE TABLE persons2content (
    PERSON_ID INTEGER REFERENCES persons (ID) ON DELETE CASCADE,
    FILM_ID INTEGER REFERENCES filmes (ID) ON DELETE CASCADE,
    PERSON_TYPE VARCHAR(512)
);

